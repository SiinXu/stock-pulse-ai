export const PLAYGROUND_ENTRY_BY_ID = new Map(PLAYGROUND_CATALOG.map((item) => [item.id, item]));

export function getPlaygroundEntry(id: string | undefined): PlaygroundEntry {
  return (id && PLAYGROUND_ENTRY_BY_ID.get(id)) || PLAYGROUND_CATALOG[0];
}

export function getPlaygroundScenario(entryValue: PlaygroundEntry, id: string | undefined): PlaygroundScenarioId {
  return entryValue.scenarios.some((item) => item.id === id)
    ? id as PlaygroundScenarioId
    : entryValue.scenarios[0].id;
}
